        printString +=              `<li class="list-group-item">1: ${e.price1}</li>`
        printString +=              `<li class="list-group-item">5-10: ${e.price2}</li>`
        printString +=              `<li class="list-group-item">10 or more: ${e.price3}</li>`
        printString +=          `</ul>`
        printString +=      `</div>`;
        printString +=  `</div>`;
